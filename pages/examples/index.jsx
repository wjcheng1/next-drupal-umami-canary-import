export async function getStaticProps(context) {
	const { locale } = context;
	const store = getCurrentLocaleStore(locale, globalDrupalStateStores);

	try {
		const footerMenu = await store.getObject({
			objectName: 'menu_items--main',
			refresh: !BUILD_MODE,
			anon: true,
		});

		return {
			props: {
				footerMenu,
			},
			revalidate: 60,
		};
	} catch (error) {
		console.error(error);
	}
}
